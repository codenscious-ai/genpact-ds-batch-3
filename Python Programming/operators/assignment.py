x = 10

x += 10       # x = x + 10
print(x)      # 20

x -= 5        # x = x - 5
print(x)      # 15 

x *= 2        # x = x * 5
print(x)      # 30

x /= 5        # x = x / 5
print(x)      # 8.0

x %= 4        # x = x % 5
print(x)      # 2.0

x **= 3       # x = x ** 3
print(x)      # 8.0

x //= 3       # x = x // 3
print(x)      # 2.0  